<h4>Visão geral</h4><hr>
<p>O módulo de arquivos é uma ótima alternativa para gerenciar os arquivos utilizados no site. 
Todas as imagens ou arquivos que são inseridos nas páginas, galerias, ou artigos do blog são armazenados aqui. 
Para inserir imagens no conteúdo da página pode enviá-los diretamente a partir do editor de texto WYSIWYG ou pode carregá-las aqui e apenas inseri-los através do WYSIWYG.</p>

<h6>Gerenciar pastas</h6>
<p>Depois de criar uma ou mais pastas em um nível mais alto, também poderá criar quantas subpastas forem necessárias, como blog/images/screenshots/image.jpg e pages/audio/sample.mp3.
Os nomes das pastas são apenas para sua organização, os nomes não são mostrados nos links para download no front-end.
Para editar uma pasta, seleccione a opção "Gerenciar pastas" no menu dentro da página arquivos, em seguida clique no botão "Editar" da pasta que precisa de alterar.</p>

<h6>Gerenciar arquivos</h6>
<p>Para gerenciar os seus arquivos seleccione uma pasta a partir do menu. Se o arquivo estiver numa subpasta basta escolher a pasta pai. 
Quando estiver na pasta pai, poderá escolher alguma subpasta e também filtrar os arquivos por tipo usando os menus suspensos na parte superior da tela.
Agora que está na pasta adequada, pode enviar arquivos clicando no botão "Envio de arquivo" ao lado dos menus suspenso de filtragem.
Observe que há duas maneiras de ver os seus arquivos: Lista e Grade. Lista mostra detalhes do arquivo, enquanto na grade irá mostrar uma miniatura da imagem.</p>

<h6>Enviar arquivos</h6>
<p>Depois de clicar no botão para iniciar o envio de arquivos na pasta desejada, uma janela de envio vai aparecer. 
Pode adiccionar arquivos de qualquer forma, arrastando do seu computador para a caixa de envio de arquivos ou clicando na caixa e escolher os arquivos a partir de sua janela de diálogo de arquivos padrão.
Os arquivos selecionados serão exibidos numa lista na parte inferior da tela.
Pode então apagar arquivos desnecessários da lista ou se preferir clique em Enviar para iniciar o processo de envio.
Se o envio não for bem sucedido verifique o tamanho do arquivo. Muitos hosts não permitem envio de arquivos superiores a 2MB.
Muitas câmeras fotograficas modernas produzem imagens maiores que 5MB.
Para remediar essa limitação pode-se solicitar ao provedor de hospedagem para alterar o limite de envio ou, pode redimensionar suas imagens antes de enviar.
Redimensionamento tem a vantagem adicional de acelerar o tempo de envio e o tempo de carregamento da página.</p>';