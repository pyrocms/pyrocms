<h4>Visão geral</h4><hr>
<p>O módulo ficheiros é uma ótima alternativa para gerir os ficheiros utilizados no site. 
Todas as imagens ou ficheiros que são inseridos nas páginas, galerias, ou artigos do blog são armazenados aqui. 
Para inserir imagens no conteúdo da página pode enviá-los diretamente a partir do editor de texto WYSIWYG ou pode carregá-las aqui e apenas inseri-los através do WYSIWYG.</p>

<h6>Gerir pastas</h6>
<p>Depois de criar uma ou mais pastas em um nível mais alto, também poderá criar quantas subpastas forem necessárias, como blog/images/screenshots/image.jpg e pages/audio/sample.mp3.
Os nomes das pastas são apenas para sua organização, os nomes não são mostrados nos links para download no front-end.
Para editar uma pasta, seleccione a opção "Gerir pastas" no menu dentro da página ficheiros, em seguida clique no botão "Editar" da pasta que precisa de alterar.</p>

<h6>Gerir ficheiros</h6>
<p>Para gerir os seus ficheiros seleccione uma pasta a partir do menu. Se o ficheiro estiver numa subpasta basta escolher a pasta pai. 
Quando estiver na pasta pai, poderá escolher alguma subpasta e também filtrar os ficheiros por tipo usando os menus suspensos na parte superior da tela.
Agora que está na pasta adequada, pode enviar ficheiros clicando no botão "Envio de ficheiro" ao lado dos menus suspenso de filtragem.
Observe que há duas maneiras de ver os seus ficheiros: Lista e Grelha. Lista mostra detalhes do ficheiro, enquanto na grelha irá mostrar uma miniatura da imagem.</p>

<h6>Enviar ficheiros</h6>
<p>Depois de clicar no botão para iniciar o envio de ficheiros na pasta desejada, uma janela de envio vai aparecer. 
Pode adiccionar ficheiros de qualquer forma, arrastando do seu computador para a caixa de envio de ficheiros ou clicando na caixa e escolher os ficheiros a partir de sua janela de diálogo de ficheiros padrão.
Os ficheiros seleccionados serão exibidos numa lista na parte inferior da tela.
Pode então apagar ficheiros desnecessários da lista ou se satisfeito clique em Enviar para iniciar o processo de envio.
Se o envio não for bem sucedido verifique o tamanho do ficheiro. Muitos hosts não permitem carregamento de ficheiros superiores a 2MB.
Muitas câmera fotograficas modernas produzem imagens maiores que 5MB.
Para remediar essa limitação pode-se solicitar ao provedor de hospedagem para alterar o limite de envio ou, pode redimensionar suas imagens antes de enviar.
Redimensionamento tem a vantagem adicional de acelerar o tempo de envio e o tempo de carregamento da página.</p>';