<h4>Overview</h4><hr>
<p>De bestanden module is een goede manier om voor de site admin bestanden te beheren.
Alle afbeeldingen of bestanden die worden geplaatst in pagina's gallerijen of blog berichten worden hier opgeslagen.
Pagina afbeeldingen kunnen direct van de WYSIWYG editor worden geupload of eerst hier worden geupload en later worden geplaatst via de WYSIWYG editor</p>

<h6>Mappen beheren</h6>
<p>Nadat u de eerste map of mappen heeft gemaakt kunt u zo veel submappen aanmaken als u wilt. Bijvoorbeeld blog/images/screenshots/image.jpg and pages/audio/sample.mp3.
De mapnamen zijn alleen voor uw gebruik, de mapnaam wordt niet weergeven in de download link.
Om een map te wijzigen kunt u Beheer mappen in het linker column selecteren. Klik dan op Wijzig op de map die u wilt wijzigen.</p>

<h6>Bestanden beheren</h6>
<p>Om bestanden te beheren selecteert u de mapnaam in de linker column. Als het bestand in een submap is kunt u de hoofdmap gebruiken.
Zodra u in de hoofdmap bent kunt u de submap kiezen en een filter toepassen om op bestandstype te filteren doormiddel van de selectie bovenaan uw scherm.
Nu in de goede map bent kunt u bestanden uploaden door op de Upload knop te drukken naast het filter.
Er zijn twee manier op uw bestanden te bekijken: Lijst en Grid. Lijst laat alle bestand details zien terwijl grid een thumbnail laat zien van de afbeelding.</p>

<h6>Bestanden uploaden</h6>
<p>
Nadat u op de upload knop heeft gedrukt in de gewenste map komt er een upload scherm.
U kunt bestanden toevoegen door deze te slepen in het Upload gedeelte of door bestanden te selecteren via het standaard upload scherm.
De gelecteerde bestanden worden weergegeven in een lijst onder aan uw scherm.
U kunt eventueele onnodige bestanden verwijderen van de lijst of de bestanden uploaden door op Upload te klikken.
