<h4>Introduzione</h4><hr>
<p>Questo modulo è un modo eccellente per permettere all'amministratore del sito di gestire in modo semplice i file da utilizzare sul sito.
Tutte le immagini o file che sono inseriti nelle pagine, gallerie o blog sono salvate qui.
Per il contenuto delle pagine puoi caricare le immagini direttamente dall'editor WYSIWYG oppure caricarle da questa sezione e poi inserirle tramite l'editor WYSIWYG.</p>

<h6>Gestione delle cartelle</h6>
<p>Dopo che hai creato la cartella o le cartelle di primo livello puoi creare quante sottocartelle desideri come ad esempio blog/images/screenshots/image.jpg e pages/audio/sample.mp3.
Il nome delle cartelle è a tuo uso esclusivo, non sarà mostrato nel link di download o nel front end.
Per modificare una cartella seleziona Gestione cartelle sulla colonna di sinistra, poi clicca Modifica sulla cartella che desideri modificare.</p>

<h6>Gestione dei file</h6>
<p>Per gestire i file seleziona il nome della cartella nella colonna di sinistra. Se il file si trova in una sottocartella devi semplicemente scegliere il padre. 
Una volta che che sei nella cartella padre puoi scegliere la sottocartella e filtrare il tipo di file dal menu a tendina  nella parte superiore dello schermo.
Ora che sei nella cartella corretta puoi caricare i file cliccando sul bottone Upload vicino al sottomenu filtri.
Nota che ci sono due modi per mostrare i file: Lista e Griglia. Lista mostra i dettagli dei file mentre la griglia mostra una anteprima dell'immagine.</p>

<h6>Caricamento dei file</h6>
<p>Dopo aver cliccato il bottone Upload nella cartella desiderata, apparirà una finestra di caricamento.
Puoi aggiungere file in due modi, trascinandoli nel box Upload File o cliccando sul box scegliendo i file da caricare attraverso la consueta finestra di upload.
I file selezionati verranno mostrati in una lista sul fondo dello schermo. 
Potrai poi cancellare i file non necessari dalla lista che apaprirà o, se sei soddisfatto, potrai caricarli premendo sul bottone Upload.
Se il caricamento non va a buon fine controlla il peso dei file. Molti host non permettono di caricare file più grandi di 2MB.
Molte delle fotocamere moderne producono immagini che superano i 5MB. 
Per sistemare questo problema potresti chiedere al tuo host di aumentare il limite di upload o potresti ridimensionare le immagini rima di caricarle.. 
Il ridimensionamento è vantaggioso in fatto di velocità.</p>";